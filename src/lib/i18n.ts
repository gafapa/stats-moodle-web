export type MessageKey = keyof typeof messages.en;

export function translate(language: LanguageCode, key: MessageKey): string {
  const localized = messages[language as keyof typeof messages] as typeof messages.en | undefined;
  return localized?.[key] ?? messages.en[key];
}

export function translateRiskLevel(language: LanguageCode, riskLevel: RiskLevel): string {
  if (riskLevel === "high") {
    return translate(language, "riskHigh");
  }
  if (riskLevel === "medium") {
    return translate(language, "riskMedium");
  }
  return translate(language, "riskLow");
}

